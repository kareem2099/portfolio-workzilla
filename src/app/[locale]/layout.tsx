import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import React from "react";
import "@/app/globals.css";
import Header from "@/components/Header";
import ThemeProvider from "@/components/ThemeProvider";
import AIChatController from "@/components/ai/AIChatController"; // Import AIChatController
import { NextIntlClientProvider } from 'next-intl';
import { getMessages } from 'next-intl/server'; // Use the correct import from next-intl

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

import { getValidLocale } from '@/lib/localeUtils';

export default async function RootLayout({
  children,
  params,
}: Readonly<{
  children: React.ReactElement<{ params: { locale: string } }>;
  params: Promise<{ locale: string }>;
}>) {
  const resolvedParams = await params;
  const locale = await getValidLocale(resolvedParams.locale);
  
  const messages = await getMessages({ locale });

  return (
    <html lang={locale} dir={locale === 'ar' ? 'rtl' : 'ltr'}>
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        <NextIntlClientProvider locale={locale} messages={messages}>
          <ThemeProvider>
            <Header locale={locale} />
            {React.cloneElement(children, { params: resolvedParams })}
            <AIChatController /> {/* Add AIChatController here */}
          </ThemeProvider>
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
